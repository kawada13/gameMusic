<template>
  <div>
        <!-- ローディング中 -->
        <div class="my-5" v-if="loading">
          <Loader />
        </div>

        <div class="card mt-2" v-if="!loading">
            <h3 class="card-header">
              購入履歴
            </h3>
            <p class="purchase_count ml-4 mt-3 mb-5">購入した商品：{{audios.length}}件</p>
            <div class="no_audio my-4" v-if="!audios.length">
              <p>現在購入されたオーディオはございません。</p>
            </div>
            <div class="audios ml-4 my-4" v-for="(audio,i) in audios" :key="i">
              <h4 class="audio_title ">{{ audio.title }}</h4>
              <h6 class="card-subtitle mb-2 text-muted creater_name">クリエイター：{{audio.user.name}}</h6>
              <h6 class="card-subtitle mb-2 text-muted" >購入日：{{audio.purchase_records[0].created_at | fromiso}}</h6>
              <h6 class="card-subtitle mb-2 price font-weight-bold text-danger"><i class="fas fa-yen-sign"></i>{{audio.price | comma}}</h6>
              <audio controls controlslist="nodownload" class="my-3">
                <source :src="audio.sample_audio_file">
              </audio>
              <div>
                 <a type="button" class="btn btn-danger font-weight-bold text-white" id="download" download :href="audio.audio_file"><i class="fas fa-download mr-2"></i>ダウンロード</a>
                 <h6 class="card-subtitle my-1 text-muted creater_name">音源のフルバージョンです。<br>リンク先でダウンロード可能です。</h6>
              </div>
            </div>
        </div>
  </div>
</template>

<script>
export default {
  data() {
    return {
      audios:[],
      loading:false
    }
  },
  methods: {
    download(url) {
      const link = document.createElement('a')
      link.download = 'result.csv'
      link.href = url
      link.click()
    },
    async getPurchases() {

      try{
        this.loading = true
        await this.$store.dispatch('purchase/getPurchases')
        this.audios = this.$store.state.purchase.purchases

      }
      catch(e){
        // console.log(e);
        this.loading = false
      }
      finally{
        this.loading = false
      }
    }

  },
  created() {
    Promise.all([
      this.getPurchases(),
    ])
  },

}
</script>

